const aliases: Record<string, string> = {
	ts: "typescript",
	js: "javascript",
	sh: "bash",
	zsh: "bash",
	yml: "yaml",
}

function LanguageIcon({ lang }: { lang: string }) {
	const normalized = aliases[lang] || lang
	const path = icons[normalized]
	if (!path) return null

	return (
		<svg
			viewBox="0 0 24 24"
			fill="currentColor"
			style={{ width: 14, height: 14, flexShrink: 0 }}
			aria-hidden="true"
		>
			<path d={path} />
		</svg>
	)
}

function CopyButton({ codeRef }: { codeRef: React.RefObject<HTMLElement | null> }) {
	const [copied, setCopied] = useState(false)

	const copy = async () => {
		const code = codeRef.current?.textContent || ""
		await navigator.clipboard.writeText(code)
		setCopied(true)
		setTimeout(() => setCopied(false), 1500)
	}

	return (
		<button
			type="button"
			onClick={copy}
			aria-label={copied ? "Copied" : "Copy code"}
			style={{
				display: "flex",
				alignItems: "center",
				justifyContent: "center",
				padding: "6px",
				color: copied ? "#22c55e" : "#737373",
				background: "transparent",
				border: "none",
				cursor: "pointer",
				transition: "color 0.15s",
				borderRadius: "4px",
			}}
			onMouseEnter={(e) => !copied && (e.currentTarget.style.color = "#fafafa")}
			onMouseLeave={(e) => !copied && (e.currentTarget.style.color = "#737373")}
		>
			{copied ? (
				<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" style={{ width: 14, height: 14 }} aria-hidden="true">
					<polyline points="20 6 9 17 4 12" />
				</svg>
			) : (
				<svg viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" style={{ width: 14, height: 14 }} aria-hidden="true">
					<rect x="9" y="9" width="13" height="13" rx="2" ry="2" />
					<path d="M5 15H4a2 2 0 0 1-2-2V4a2 2 0 0 1 2-2h9a2 2 0 0 1 2 2v1" />
				</svg>
			)}
		</button>
	)
}

interface CodeBlockProps {
	children: ReactNode
	lang?: string
	title?: string
}

export function CodeBlock({ children, lang, title }: CodeBlockProps) {
	const codeRef = useRef<HTMLDivElement>(null)
	const label = title || lang

	return (
		<figure
			style={{
				position: "relative",
				margin: "24px 0",
				borderRadius: "8px",
				backgroundColor: "#0d0d0d",
				border: "1px solid #1c1c1c",
				overflow: "hidden",
			}}
		>
			{label && (
				<div
					style={{
						display: "flex",
						alignItems: "center",
						justifyContent: "space-between",
						padding: "0 12px",
						height: "36px",
						borderBottom: "1px solid #1c1c1c",
					}}
				>
					<div style={{ display: "flex", alignItems: "center", gap: "8px" }}>
						{lang && <LanguageIcon lang={lang} />}
						<span style={{ fontSize: "12px", color: "#737373" }}>{label}</span>
					</div>
					<CopyButton codeRef={codeRef} />
				</div>
			)}
			<div
				ref={codeRef}
				className="[&::-webkit-scrollbar]:hidden [-ms-overflow-style:none] [scrollbar-width:none]"
				style={{
					padding: "14px 16px",
					overflow: "auto",
					fontSize: "13px",
					lineHeight: "1.6",
					maxHeight: "500px",
				}}
			>
				{children}
			</div>
			{!label && (
				<div style={{ position: "absolute", top: "8px", right: "8px" }}>
					<CopyButton codeRef={codeRef} />
				</div>
			)}
		</figure>
	)
}
